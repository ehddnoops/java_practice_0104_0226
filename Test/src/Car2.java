
public class Car2 {
	
	String color;
	int cc;
	int maxSpeed;
	String model;
	
	
	
	public Car2(){
		
	}
	
	public Car2(String color) {
		this.color=color;
	}

	public Car2(String color, int cc) {
		this.color = color;
		this.cc = cc;
	}
	public Car2(String color, int cc,int maxSpeed) {
		this.color = color;
		this.cc = cc;
		this.maxSpeed = maxSpeed;
	}
	public Car2(String color,String model) {
		this.color=color;
		this.model=model;
	}
	
	
	
	
}
